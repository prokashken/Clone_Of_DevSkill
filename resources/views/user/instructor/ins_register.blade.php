            return true;
        } else if (time[0] === "" || time[1] === "") {
            return true;
        } else if (time[0][1] === "_" || time[1][1] === "_") {
            return true;
        }
        
        return false;
    }

    function validateTime(e) {
        let time = e.target.value;
        // Took logic in separate method to be used during form submission
